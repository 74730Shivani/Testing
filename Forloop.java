package com;

public class Forloop {

	public static void main(String[] args) {
	 for(int i=0; i<=20;i+=5)
	 {
		System.out.print(i+" ");//forward loop with increment by 5 
	 }
	 System.out.println();
	 
	 for(int i=20;i>=0;i-=5) {//backward loop with decrement by 5
		System.out.print(i+" ");
		 
	 }
	}

}
